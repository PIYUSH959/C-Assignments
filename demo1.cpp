#include<iostream>

using namespace std;

int main()
{
    unsigned int a = 5 , b = 9;

    cout << "\n a = " << a << " b = " << b ;

    cout << "\n a << 1 " << (a << 1);

    cout << "\n a >> 1 " << (a >> 1);

    cout << "\n a & b " << (a & b);

    cout << "\n a / b " << (a / b);

    cout << "\n a ^ b " << (a ^ b);

    cout << "\n ~ a" << (~a);

    return 0;
}
